`                               NODEMAILER                              `/*

Para poder realizar envío de mensajería a partir de nuestros aplicativos
la librería por defecto que se utiliza es nodemailer, éste nos permitirá
conectar con múltiples hosts y servicios de mailing. 

Crear un "transporte" para poder enviar mails a lo largo de mi app.

*/

`                                 TWILIO                                `/*

Twilio es una plataforma de comunicaciones que nos permitirá establecer
un sistema de comunicación más directo con el cliente.

En muchas ocasiones, nuestros correos podrían dar a parar a la bandeja
de spam del cliente (Según cómo lo tenga configurado), de modo que ocupa-
remos realizar mensajería más directo con éste, según sea la intención de
nuestro aplicativo. 

Twilio nos permitirá establecer SMS, whatsapp, chatbots, mensajes de voz 
pregrabados, etc.

*/

`                                 MOCK                                  `/*

Dentro de sus múltiples traducciones, podemos tomar mock como una 
"imitación" de un dato real. Nos es altamente útil para poder crear datos
supuestos con el fin de probar la funcionalidad de alguna función. 

Un dato mock no debe comprometer jamás una estructura productiva, por lo 
que solo se usa en entornos de desarrollo. 


*/
